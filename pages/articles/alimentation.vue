    <p>Les pratiques alimentaires durables sont celles qui visent à réduire l'impact environnemental tout en maintenant des niveaux de production élevés et la santé des animaux. Ces pratiques peuvent inclure l'utilisation de fourrages locaux et l'intégration d'aliments alternatifs qui minimisent l'empreinte écologique de la production.</p>

    <h3>Utilisation de Fourrages Locaux et Sains</h3>
    <p>En privilégiant l'alimentation à base de fourrages locaux, les agriculteurs peuvent réduire leur dépendance aux cultures importées et aux aliments concentrés. De plus, l'utilisation de fourrages cultivés sans pesticides ni engrais chimiques contribue à préserver la santé des sols et à réduire la pollution.</p>

